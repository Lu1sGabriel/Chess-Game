package src.main.java.utils;

/**
 * Classe utilitária para definir cores ANSI para formatação de texto no terminal.
 * Esta classe fornece constantes para alterar a cor do texto e do fundo no terminal
 * usando códigos ANSI.
 */
@SuppressWarnings("unused")
public class TerminalColor {

    /**
     * Código ANSI para resetar a cor.
     */
    public static final String ANSI_RESET = "\u001B[0m";

    /**
     * Código ANSI para definir a cor do texto para preto.
     */
    public static final String ANSI_BLACK = "\u001B[30m";

    /**
     * Código ANSI para definir a cor do texto para vermelho.
     */
    public static final String ANSI_RED = "\u001B[31m";

    /**
     * Código ANSI para definir a cor do texto para verde.
     */
    public static final String ANSI_GREEN = "\u001B[32m";

    /**
     * Código ANSI para definir a cor do texto para amarelo.
     */
    public static final String ANSI_YELLOW = "\u001B[33m";

    /**
     * Código ANSI para definir a cor do texto para azul.
     */
    public static final String ANSI_BLUE = "\u001B[34m";

    /**
     * Código ANSI para definir a cor do texto para roxo.
     */
    public static final String ANSI_PURPLE = "\u001B[35m";

    /**
     * Código ANSI para definir a cor do texto para ciano.
     */
    public static final String ANSI_CYAN = "\u001B[36m";

    /**
     * Código ANSI para definir a cor do texto para branco.
     */
    public static final String ANSI_WHITE = "\u001B[37m";

    /**
     * Código ANSI para definir a cor do fundo para preto.
     */
    public static final String ANSI_BLACK_BACKGROUND = "\u001B[40m";

    /**
     * Código ANSI para definir a cor do fundo para vermelho.
     */
    public static final String ANSI_RED_BACKGROUND = "\u001B[41m";

    /**
     * Código ANSI para definir a cor do fundo para verde.
     */
    public static final String ANSI_GREEN_BACKGROUND = "\u001B[42m";

    /**
     * Código ANSI para definir a cor do fundo para amarelo.
     */
    public static final String ANSI_YELLOW_BACKGROUND = "\u001B[43m";

    /**
     * Código ANSI para definir a cor do fundo para azul.
     */
    public static final String ANSI_BLUE_BACKGROUND = "\u001B[44m";

    /**
     * Código ANSI para definir a cor do fundo para roxo.
     */
    public static final String ANSI_PURPLE_BACKGROUND = "\u001B[45m";

    /**
     * Código ANSI para definir a cor do fundo para ciano.
     */
    public static final String ANSI_CYAN_BACKGROUND = "\u001B[46m";

    /**
     * Código ANSI para definir a cor do fundo para branco.
     */
    public static final String ANSI_WHITE_BACKGROUND = "\u001B[47m";

}

